# A tuple in Python is an immutable ordered collection of elements.
tup = ()
print(tup)

# Using String
tup = ('Geeks', 'For')
print(tup[0])

# Using List
li = [1, 2, 4, 5, 6]
tli = tuple(li)
print(tli)

# Using Built-in Function
tup = tuple('Geeks')
print(tup)

# Iterating Tuples
print("--------------------")
for i in li:
    print(i)

